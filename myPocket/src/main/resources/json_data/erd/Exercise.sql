DROP TABLE "EXERCISE";

CREATE TABLE "EXERCISE" (
	"EXERCISE_ID"	NUMBER		NOT NULL,
	"CLASSIFICATION"	VARCHAR		NULL,
	"NAME"	VARCHAR		NULL,
	"GIF"	CLOB		NULL
);

DROP TABLE "MUSCLES";

CREATE TABLE "MUSCLES" (
	"MUSCLE_ID"	NUMBER		NOT NULL,
	"MUSCLE_ABBR"	VARCHAR		NULL,
	"ExerciseID"	NUMBER		NOT NULL
);

DROP TABLE "EQUIPMENT";

CREATE TABLE "EQUIPMENT" (
	"EQUIPMENT_ID"	NUMBER		NOT NULL,
	"NAME"	VARCHAR		NULL,
	"ExerciseID"	NUMBER		NOT NULL
);

DROP TABLE "INSTRUCTIONS";

CREATE TABLE "INSTRUCTIONS" (
	"INSTRUCTION_ID"	NUMBER		NOT NULL,
	"DESCRIPTION"	CLOB		NULL,
	"ExerciseID"	NUMBER		NOT NULL
);


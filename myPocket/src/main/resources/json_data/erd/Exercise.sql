	"MUSCLE_ID"
);

ALTER TABLE "EQUIPMENT" ADD CONSTRAINT "PK_EQUIPMENT" PRIMARY KEY (
	"EQUIPMENT_ID"
);

ALTER TABLE "INSTRUCTIONS" ADD CONSTRAINT "PK_INSTRUCTIONS" PRIMARY KEY (
	"INSTRUCTION_ID"
);

